//write examples for loops in JavaScript
//for loop
console.log("For Loop Example:");
for (let i = 1; i <= 5; i++) {
    console.log(i);
}
//while loop
console.log("While Loop Example:");
let j = 1;
while (j <= 5) {
    console.log(j);
    j++;
}
//do-while loop
console.log("Do-While Loop Example:");
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 5);