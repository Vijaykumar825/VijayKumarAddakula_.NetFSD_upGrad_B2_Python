let a=2;
let b=3;
//arithmetic operators
console.log("a + b =", a + b); // addition
console.log("a - b =", a - b); // subtraction
console.log("a * b =", a * b);// multiplication
console.log("a / b =", a / b); // division
console.log("a % b =", a % b); // modulus
console.log("a ** b =", a ** b); // exponentiation
//assignment operators
a += 1; // a = a + 1
console.log("a after a += 1:", a);
a -= 1; // a = a - 1
console.log("a after a -= 1:", a);
a *= 2; // a = a * 2 
console.log("a after a *= 2:", a);
a /= 2;
console.log("a after a /= 2:", a);
a %= 2;
console.log("a after a %= 2:", a);
a **= 2;
console.log("a after a **= 2:", a); 
//comparison operators
console.log("a == b:", a == b); // equality
console.log("a === b:", a === b); // strict equality
console.log("a != b:", a != b); // inequality     
console.log("a !== b:", a !== b); // strict inequality
console.log("a > b:", a > b); // greater than
console.log("a < b:", a < b); // less than
console.log("a >= b:", a >= b);
console.log("a <= b:", a <= b);
//logical operators
let x = true;     
let y = false;
console.log("x && y:", x && y); // logical AND
console.log("x || y:", x || y); // logical OR
console.log("!x:", !x); // logical NOT    


